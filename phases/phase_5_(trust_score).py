# Load BiLSTM test sequences
X_test_seq = joblib.load("/content/drive/MyDrive/phishing_project_datasets/X_test_seq.pkl")

# Load Random Forest test features
X_test_url = joblib.load("/content/drive/MyDrive/phishing_project_datasets/X_test_url.pkl")

