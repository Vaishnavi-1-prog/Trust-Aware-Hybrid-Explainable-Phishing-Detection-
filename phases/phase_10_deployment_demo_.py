def update_confidence_adjustment(input_hash, is_correct):
    """
    Reinforce (+) or penalise (-) confidence for a specific input hash.
    """
    delta = +0.05 if is_correct else -0.10
    conn  = sqlite3.connect(DB_PATH)
    c     = conn.cursor()
    now   = datetime.utcnow().isoformat()
    c.execute("""
        INSERT INTO confidence_adjustments (input_hash, adjustment, count, updated)
        VALUES (?, ?, 1, ?)
        ON CONFLICT(input_hash) DO UPDATE SET
            adjustment = MAX(-0.50, MIN(0.50, adjustment + excluded.adjustment)),
            count      = count + 1,
            updated    = excluded.updated
    """, (input_hash, delta, now))
    conn.commit()
    conn.close()


def get_confidence_adjustment(input_hash):
    """Return stored adjustment or 0.0."""
    conn = sqlite3.connect(DB_PATH)
    row  = conn.execute(
        "SELECT adjustment FROM confidence_adjustments WHERE input_hash = ?",
        (input_hash,)
    ).fetchone()
    conn.close()
    return float(row[0]) if row else 0.0


