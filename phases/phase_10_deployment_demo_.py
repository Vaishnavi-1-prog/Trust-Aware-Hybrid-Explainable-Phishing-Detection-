def init_db():
    """Initialise SQLite database for feedback storage."""
    conn = sqlite3.connect(DB_PATH)
    c = conn.cursor()

    # --- Feedback table ---
    c.execute("""
        CREATE TABLE IF NOT EXISTS feedback (
            id            INTEGER PRIMARY KEY AUTOINCREMENT,
            input_hash    TEXT,
            url           TEXT,
            header        TEXT,
            body          TEXT,
            model_verdict TEXT,
            trust_score   INTEGER,
            correct_label TEXT,
            is_correct    INTEGER,
            timestamp     TEXT
        )
    """)

    # --- Adaptive keywords table ---
    c.execute("""
        CREATE TABLE IF NOT EXISTS adaptive_keywords (
            keyword   TEXT PRIMARY KEY,
            category  TEXT,          -- 'suspicious' or 'trusted'
            weight    REAL,
            frequency INTEGER DEFAULT 1,
            updated   TEXT
        )
    """)

    # --- Confidence adjustments table ---
    c.execute("""
        CREATE TABLE IF NOT EXISTS confidence_adjustments (
            input_hash TEXT PRIMARY KEY,
            adjustment REAL,         -- positive = boost trust, negative = reduce
            count      INTEGER DEFAULT 1,
            updated    TEXT
        )
    """)

    conn.commit()
    conn.close()
    print("✅ Feedback DB initialised.")

init_db()


def compute_hash(url, body):
    """Stable hash for (url, body) pair."""
    raw = f"{(url or '').strip().lower()}|{(body or '').strip().lower()}"
    return hashlib.md5(raw.encode()).hexdigest()


def save_feedback(url, header, body, model_verdict, trust_score,
                  is_correct, correct_label=None):
    """Persist one feedback record."""
    conn = sqlite3.connect(DB_PATH)
    c = conn.cursor()
    c.execute("""
        INSERT INTO feedback
            (input_hash, url, header, body, model_verdict, trust_score,
             correct_label, is_correct, timestamp)
        VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?)
    """, (
        compute_hash(url, body),
        url or "", header or "", body or "",
        model_verdict, int(trust_score),
        correct_label or "", int(is_correct),
        datetime.utcnow().isoformat()
    ))
    conn.commit()
    conn.close()


def load_all_feedback():
    """Return all feedback rows as a list of dicts."""
    conn = sqlite3.connect(DB_PATH)
    conn.row_factory = sqlite3.Row
    rows = conn.execute("SELECT * FROM feedback").fetchall()
    conn.close()
    return [dict(r) for r in rows]


