def _empty_fig(title=""):
    fig = go.Figure()
    fig.update_layout(title=title, paper_bgcolor="#0d1117", font_color="white")
    return fig


def risk_gauge(score):
    color = "red" if score<=4 else ("orange" if score<=6 else "green")
    fig   = go.Figure(go.Indicator(
        mode="gauge+number", value=score,
        title={"text":"Trust Score","font":{"color":"white"}},
        gauge={"axis":{"range":[0,10],"tickcolor":"white"},
               "bar":{"color":color},"bgcolor":"#0d1117",
               "steps":[{"range":[0,4],"color":"#ff4c4c"},
                        {"range":[4,7],"color":"#ffa500"},
                        {"range":[7,10],"color":"#2ecc71"}]}
    ))
    fig.update_layout(paper_bgcolor="#0d1117",font_color="white")
    return fig


def url_feature_chart(url):
    if rf_model is None:
        return _empty_fig("RF not loaded")
    try:
        imps = rf_model.feature_importances_
        n    = min(len(imps), 41)
        df   = pd.DataFrame({"feature":[f"F{i}" for i in range(n)],
                              "importance":imps[:n]
                             }).sort_values("importance",ascending=False).head(10)
        fig  = go.Figure(go.Bar(x=df.importance,y=df.feature,
                                orientation='h',marker_color='crimson'))
        fig.update_layout(title="Top RF URL Features",
                          plot_bgcolor="#0d1117",paper_bgcolor="#0d1117",
                          font_color="white")
        return fig
    except Exception as e:
        return _empty_fig(f"Chart error: {e}")


def word_chart(text):
    try:
        SW = {"the","a","an","is","in","it","of","to","and","or","for","on",
              "with","was","are","this","that","i","you","we","he","she","they",
              "at","be","by","as","so","if","but","not","from","your","our",
              "will","have","has","had","do","did","my","me","us","dear","re"}
        words    = re.findall(r'\b[a-z]{3,}\b', text.lower())
        filtered = [w for w in words if w not in SW]
        freq     = pd.Series(filtered or words).value_counts().head(10)
        fig = go.Figure(go.Bar(x=freq.values,y=freq.index,
                               orientation='h',marker_color='royalblue'))
        fig.update_layout(title="Top Message Keywords",
                          plot_bgcolor="#0d1117",paper_bgcolor="#0d1117",
                          font_color="white")
        return fig
    except Exception as e:
        return _empty_fig(f"Chart error: {e}")


def learned_domains_chart():
    try:
        conn = sqlite3.connect(DB_PATH)
        rows = conn.execute(
            "SELECT suffix,count,promoted FROM trusted_domain_patterns "
            "ORDER BY count DESC LIMIT 20"
        ).fetchall()
        conn.close()
        if not rows:
            return _empty_fig("No domain patterns learned yet")
        suffixes = [r[0] for r in rows]
        counts   = [r[1] for r in rows]
        colors   = ["#2ecc71" if r[2] else "#ffa500" for r in rows]
        fig = go.Figure(go.Bar(x=counts,y=suffixes,orientation='h',
                               marker_color=colors))
        fig.update_layout(
            title="Learned Domain Suffixes "
                  "(green=auto-trusted, orange=pending 2nd confirmation)",
            plot_bgcolor="#0d1117",paper_bgcolor="#0d1117",
            font_color="white", xaxis_title="Confirmed legitimate domains")
        return fig
    except:
        return _empty_fig("Domain chart error")


