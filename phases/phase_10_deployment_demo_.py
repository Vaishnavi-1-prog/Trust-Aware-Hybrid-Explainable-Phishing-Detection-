HARD_PHISH_GATE = 0.65
SOFT_PHISH_GATE = 0.45
SPAM_GATE       = 0.40
CONFLICT_THR    = 0.35


def three_way_arbiter(phish_risk, spam_score, header_risk, expl):
    """
    Mutually exclusive gates. Returns (trust_score, verdict, class).
    expl is a list that gets appended with gate reasoning.
    """
    # Gate A — hard phishing
    if phish_risk >= HARD_PHISH_GATE:
        trust = max(0, min(3, int((1.0-phish_risk)*6)))
        if header_risk and header_risk > 0.40:
            trust = max(0, trust-1)
        expl.append(f"🚨 Gate A fired: phish_risk={phish_risk:.3f} "
                    f"≥ {HARD_PHISH_GATE} → PHISHING")
        return trust, "🚨 PHISHING — High Risk", "PHISHING"

    # Gate B — spam (only if phishing gate did NOT fire)
    if spam_score >= SPAM_GATE:
        trust = max(4, min(6, int(6-(spam_score-0.40)*5)))
        expl.append(f"⚠️ Gate B fired: spam={spam_score:.3f} "
                    f"≥ {SPAM_GATE}, phish={phish_risk:.3f} < {HARD_PHISH_GATE}"
                    f" → SPAM")
        return trust, "⚠️ SPAM / UNSOLICITED", "SPAM"

    # Gate C — soft phishing
    if phish_risk >= SOFT_PHISH_GATE:
        trust = max(2, min(4, int((1.0-phish_risk)*10)))
        expl.append(f"🔶 Gate C fired: phish_risk={phish_risk:.3f} "
                    f"≥ {SOFT_PHISH_GATE} → PHISHING (medium)")
        return trust, "🚨 PHISHING — Medium Risk", "PHISHING"

    # Gate D — legitimate
    trust = max(7, min(10, int((1.0-phish_risk)*10)))
    if header_risk and header_risk > 0.30:
        trust = max(7, trust-1)
    expl.append(f"✅ Gate D: all clear (phish={phish_risk:.3f}, "
                f"spam={spam_score:.3f}) → LEGITIMATE")
    return trust, "✅ LIKELY LEGITIMATE", "LEGITIMATE"


