def build_feedback_index():
    """Build TF-IDF index over all stored feedback texts."""
    rows = load_all_feedback()
    if not rows:
        return None, []
    docs = [f"{r['url']} {r['body']}" for r in rows]
    try:
        vec    = TfidfVectorizer(max_features=500, stop_words='english')
        matrix = vec.fit_transform(docs)
        return (vec, matrix), rows
    except Exception:
        return None, rows


def query_feedback_memory(url, body, threshold=0.82):
    """
    Check if a similar (url, body) exists in feedback memory.
    Returns (best_match_row, similarity_score) or (None, 0).
    """
    query_text = f"{url or ''} {body or ''}".strip()
    if not query_text:
        return None, 0.0

    (vec_and_mat), rows = build_feedback_index()
    if vec_and_mat is None or not rows:
        return None, 0.0

    vec, matrix = vec_and_mat
    try:
        q_vec = vec.transform([query_text])
        sims  = cosine_similarity(q_vec, matrix).flatten()
        best_idx   = int(np.argmax(sims))
        best_score = float(sims[best_idx])
        if best_score >= threshold:
            return rows[best_idx], best_score
    except Exception:
        pass
    return None, 0.0


