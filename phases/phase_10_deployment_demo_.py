def get_rf_score(url):
    """Returns phishing probability from RF, or None."""
    if not url or rf_model is None:
        return None
    try:
        parsed = urlparse(url)
        host   = parsed.netloc or ""
        path   = parsed.path   or ""
        query  = parsed.query  or ""
        f = [
            len(url), url.count('.'), url.count('-'), url.count('@'),
            url.count('?'), url.count('%'), url.count('='), url.count('&'),
            url.count('_'), url.count('/'), url.count('#'), url.count('~'),
            len(host), host.count('.'), host.count('-'),
            len(path), len(query), len(parsed.scheme),
            1 if parsed.scheme=="https" else 0,
            1 if re.search(r'\d+\.\d+\.\d+\.\d+', url) else 0,
        ]
        for kw in PHISHING_URL_KEYWORDS:
            f.append(1 if kw in url.lower() else 0)

        def entropy(s):
            if not s: return 0
            return -sum((s.count(c)/len(s))*np.log2(s.count(c)/len(s))
                        for c in set(s) if s.count(c)>0)
        f += [entropy(host), entropy(path),
              sum(c.isdigit() for c in url)/max(len(url),1)]
        f = np.array(f, dtype=float)
        exp = rf_model.n_features_in_
        if len(f) < exp: f = np.concatenate([f, np.zeros(exp-len(f))])
        elif len(f) > exp: f = f[:exp]
        proba = rf_model.predict_proba(f.reshape(1,-1))[0]
        return float(proba[RF_PHISHING_IDX])
    except Exception as e:
        print(f"RF error: {e}"); return None


def get_bilstm_score(body):
    """Returns phishing probability from BiLSTM, or None."""
    if not body or bilstm is None or tokenizer is None:
        return None
    try:
        seq = tokenizer.texts_to_sequences([body])
        seq = tf.keras.preprocessing.sequence.pad_sequences(seq, maxlen=MAX_LEN)
        raw = bilstm.predict(seq, verbose=0)[0]
        return float(raw[BILSTM_PHISH_IDX] if BILSTM_MULTICLASS else raw[0])
    except Exception as e:
        print(f"BiLSTM error: {e}"); return None


HEADER_SIGNALS = {
    "spf fail":3,"dkim fail":3,"dmarc fail":3,"spoof":3,"relay":1,
    "unauthorized":3,"fake":3,"suspicious":2,"blacklisted":4,"forged":4,
    "x-spam-flag: yes":4,"x-spam-status: yes":4,
}

def get_header_score(header):
    """Returns (risk 0–1, hits list) from header rules."""
    if not header:
        return None, []
    score, hits = 0, []
    hl = header.lower()
    for sig,w in HEADER_SIGNALS.items():
        if sig in hl:
            score += w; hits.append(f"{sig}(+{w})")
    fm = re.search(r'from:\s*[^@\s]*@([\w.\-]+)', hl)
    rm = re.search(r'reply-to:\s*[^@\s]*@([\w.\-]+)', hl)
    if fm and rm and fm.group(1) != rm.group(1):
        score += 4; hits.append(f"Reply-To≠From(+4)")
    priv = re.search(
        r'received:.*?(192\.168\.\d+\.\d+|10\.\d+\.\d+\.\d+'
        r'|172\.(?:1[6-9]|2\d|3[01])\.\d+\.\d+|127\.\d+\.\d+\.\d+)', hl)
    if priv:
        score += 4; hits.append(f"PrivateIP({priv.group(1)})(+4)")
    if re.search(r'received:.*?unknown', hl):
        score += 2; hits.append("UnknownHost(+2)")
    if 'http://' in hl:
        score += 1; hits.append("NonHTTPS(+1)")
    # Header score → 0-1 risk (cap at 12 points = 1.0)
    return min(score/12.0, 1.0), hits


