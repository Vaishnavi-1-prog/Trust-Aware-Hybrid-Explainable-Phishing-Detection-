def handle_feedback_yes():
    cache = _last_prediction_cache
    if not cache:
        return (gr.update(visible=False), gr.update(visible=False),
                "⚠️ No prediction to confirm.")
    save_feedback(url=cache["url"],header=cache["header"],body=cache["body"],
                  model_verdict=cache["verdict"],trust_score=cache["score"],
                  is_correct=1)
