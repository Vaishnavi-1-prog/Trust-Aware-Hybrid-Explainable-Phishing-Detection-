def handle_correct_label(correct_label):
    cache = _last_prediction_cache
    if not cache:
        return gr.update(visible=False), "⚠️ No prediction in memory."
    save_feedback(url=cache["url"],header=cache["header"],body=cache["body"],
                  model_verdict=cache["verdict"],trust_score=cache["score"],
                  is_correct=0,correct_label=correct_label)
    update_confidence_adjustment(cache["hash"], is_correct=False)
    # Learn from BODY only
    update_adaptive_keywords(cache["body"], correct_label)
    # Learn domain pattern from URL
    promoted = learn_domain_pattern(cache["url"], correct_label)
    msg = f"✅ Correction saved! Label='{correct_label}' recorded."
    if promoted:
        msg += f" Suffix '.{promoted}' is now AUTO-TRUSTED for all future URLs!"
    else:
        msg += " Rules updated."
    return gr.update(visible=False), msg


