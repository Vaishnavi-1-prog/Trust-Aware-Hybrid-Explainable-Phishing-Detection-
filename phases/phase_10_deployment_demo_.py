def update_adaptive_keywords(text, correct_label):
    """
    Extract meaningful tokens from incorrect-prediction text.
    Map them to 'suspicious' (phishing/spam) or 'trusted' (legitimate).
    """
    STOPWORDS = {
        "the","a","an","is","in","it","of","to","and","or","for","on",
        "with","was","are","this","that","i","you","we","he","she","they",
        "at","be","by","as","so","if","but","not","from","your","our",
        "will","have","has","had","do","did","my","me","us","his","her",
        "its","their","been","all","can","just","about","up","out","no",
        "re","dear","thank","please","hi","hello"
    }
    tokens = re.findall(r'\b[a-z]{4,}\b', (text or "").lower())
    keywords = [t for t in tokens if t not in STOPWORDS]

    if correct_label in ("Phishing", "Spam"):
        category = "suspicious"
    else:
        category = "trusted"

    conn = sqlite3.connect(DB_PATH)
    c = conn.cursor()
    now = datetime.utcnow().isoformat()
    for kw in set(keywords[:30]):           # cap at 30 per feedback
        c.execute("""
            INSERT INTO adaptive_keywords (keyword, category, weight, frequency, updated)
            VALUES (?, ?, 0.05, 1, ?)
            ON CONFLICT(keyword) DO UPDATE SET
                frequency = frequency + 1,
                weight    = MIN(weight + 0.01, 0.30),
                updated   = excluded.updated
        """, (kw, category, now))
    conn.commit()
    conn.close()


def get_adaptive_keyword_lists():
    """Return (suspicious_keywords, trusted_keywords) as sets."""
    conn = sqlite3.connect(DB_PATH)
    rows = conn.execute(
        "SELECT keyword, category FROM adaptive_keywords WHERE weight >= 0.05"
    ).fetchall()
    conn.close()
    suspicious = {r[0] for r in rows if r[1] == "suspicious"}
    trusted    = {r[0] for r in rows if r[1] == "trusted"}
    return suspicious, trusted


def adaptive_keyword_score(text, url):
    """
    Returns a float risk delta in [-0.20, +0.20] based on learned keywords.
    Positive = more phishing risk.
    """
    suspicious_kw, trusted_kw = get_adaptive_keyword_lists()
    if not suspicious_kw and not trusted_kw:
        return 0.0

    combined = f"{(text or '')} {(url or '')}".lower()
    tokens   = set(re.findall(r'\b[a-z]{4,}\b', combined))

    sus_hits   = tokens & suspicious_kw
    trust_hits = tokens & trusted_kw
    delta      = (len(sus_hits) * 0.04) - (len(trust_hits) * 0.04)
    return max(-0.20, min(0.20, delta))


