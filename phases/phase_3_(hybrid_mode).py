# RANDOM FOREST PREDICTION
import numpy as np

# Example dummy features (replace later with real features)
rf_input = np.random.rand(1, rf_model.n_features_in_)

# Get probabilities
rf_prediction = rf_model.predict_proba(rf_input)

print("Random Forest Raw Prediction:")
print(rf_prediction)

classes = ["Legitimate", "Spam", "Phishing"]

print("\nClass Probabilities:")

for i, prob in enumerate(rf_prediction[0]):
    print(f"{classes[i]}: {prob:.4f}")

#HYBRID MODEL FUSION

# Random Forest probability
rf_prob = rf_prediction[0][1]

# BiLSTM probability
bilstm_prob = bilstm_prediction[0][0]

print("Random Forest Phishing Probability:", rf_prob)
print("BiLSTM Phishing Probability:", bilstm_prob)

# Hybrid weights
rf_weight = 0.4
bilstm_weight = 0.6

# Hybrid fusion
final_prob = (rf_weight * rf_prob) + (bilstm_weight * bilstm_prob)

print("\nHybrid Probability:", final_prob)

# Final decision
if final_prob > 0.5:
    print("Final Prediction: PHISHING")
else:
    print("Final Prediction: LEGITIMATE")