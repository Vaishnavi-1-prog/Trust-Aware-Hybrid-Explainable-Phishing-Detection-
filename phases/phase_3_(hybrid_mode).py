# LOAD LIBRARIES AND SAVED MODELS


import numpy as np
import joblib
import pickle
from tensorflow.keras.models import load_model

print("Loading models...")

# Load Random Forest model
rf_model = joblib.load("/content/drive/MyDrive/phishing_project_datasets/rf_phishing_model.pkl")

# Load BiLSTM model
bilstm_model = load_model("/content/drive/MyDrive/phishing_project_datasets/bilstm_email_model.h5")

print("Models loaded successfully.")

# LOAD TOKENIZER

print("Loading tokenizer...")

import pickle

with open("/content/drive/MyDrive/phishing_project_datasets/tokenizer.pkl", "rb") as f:
    tokenizer = pickle.load(f)

print("Tokenizer loaded successfully.")

"""Convert Email Text → Sequence (BiLSTM Input)
🎯 Aim

Deep learning models cannot read text directly.
So we convert the email sentence into numerical tokens using the tokenizer and then pad it to a fixed length.
"""

# TEXT → SEQUENCE CONVERSION

from tensorflow.keras.preprocessing.sequence import pad_sequences

# maximum length used during training
max_length = 200

# Sample email for testing
email_text = "Dear user your account has been suspended please click the link below to verify your account immediately"

print("Email Text:")
print(email_text)

# Convert text to sequence
sequence = tokenizer.texts_to_sequences([email_text])

# Apply padding
padded_sequence = pad_sequences(sequence, maxlen=max_length)

print("\nTokenized Sequence:")
print(sequence)

print("\nPadded Sequence Shape:")
print(padded_sequence.shape)

"""Get Prediction from BiLSTM Model
🎯 Aim

Use the BiLSTM model to predict probabilities for the three classes:
"""

# BILSTM PREDICTION

# Get prediction from BiLSTM
bilstm_prediction = bilstm_model.predict(padded_sequence)

print("BiLSTM Raw Prediction:")
print(bilstm_prediction)

# Convert to readable format
classes = ["Legitimate", "Spam", "Phishing"]

print("\nClass Probabilities:")

for i, prob in enumerate(bilstm_prediction[0]):
    print(f"{classes[i]}: {prob:.4f}")

"""since we recive binary outbut we now convert them to this model textual"""

# (CORRECTED FOR BINARY MODEL)

#BILSTM Prediction


bilstm_prediction = bilstm_model.predict(padded_sequence)

prob = bilstm_prediction[0][0]

print("BiLSTM Probability:", prob)

if prob > 0.5:
    print("BiLSTM Prediction: Phishing")
else:
    print("BiLSTM Prediction: Legitimate")

